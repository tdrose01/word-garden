export const curatedDictionary = [...new Set(WORDS.map(normalizeWord))].sort();
export const dictionarySet = new Set(curatedDictionary);

export function isDictionaryWord(word) {
  return dictionarySet.has(normalizeWord(word));
}

export function getBuildableDictionaryWords(letters, options = {}) {
  const minLength = options.minLength ?? 3;
  return curatedDictionary.filter((word) => word.length >= minLength && canBuildWord(word, letters));
}
